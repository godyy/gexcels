// indentCSharpText 为多行模版片段统一追加缩进。
// 它会对非空行添加指定数量的前导空格，方便在大模版中嵌套子模版结果。
func indentCSharpText(spaces int, text string) string {
	if text == "" {
		return ""
	}
	prefix := strings.Repeat(" ", spaces)
	lines := strings.Split(text, "\n")
	for i, line := range lines {
		if line == "" {
			continue
		}
		lines[i] = prefix + line
	}
	return strings.Join(lines, "\n")
}

// formatCSharpValue 将 Go 值转换为 C# 字面量文本。
// 主要供枚举值、常量值等模版直接输出使用。
func formatCSharpValue(value any) string {
	switch v := value.(type) {
	case string:
		return strconv.Quote(v)
	case int32:
		return strconv.FormatInt(int64(v), 10)
	case int64:
		return strconv.FormatInt(v, 10)
	case float32:
		return strconv.FormatFloat(float64(v), 'f', -1, 32) + "f"
	case float64:
		return strconv.FormatFloat(v, 'f', -1, 64)
	case bool:
		if v {
			return "true"
		}
		return "false"
	default:
		return fmt.Sprintf("%v", value)
	}
}

// formatCSharpXmlDocComment 清理并转义 XML 文档注释内容，避免破坏 C# XML 注释结构。
func formatCSharpXmlDocComment(comment string) string {
	comment = sanitizeCSharpComment(comment)
	if comment == "" {
		return ""
	}
	replacer := strings.NewReplacer(
		"&", "&amp;",
		"<", "&lt;",
		">", "&gt;",
		`"`, "&quot;",
		"'", "&apos;",
	)
	return replacer.Replace(comment)
}

// formatCSharpXmlDocBlock 生成完整的 C# XML 文档注释块，并在结尾补一个换行。
func formatCSharpXmlDocBlock(comment string) string {
	comment = sanitizeCSharpComment(comment)
	if comment == "" {
		return ""
	}
	comment = formatCSharpXmlDocComment(comment)
	return "/// <summary>\n/// " + comment + "\n/// </summary>"
}

// executeCSharpTemplate 执行指定模版，并统一补充错误上下文。
func executeCSharpTemplate(name string, tpl *template.Template, data any) string {
	var sb strings.Builder
	if err := tpl.Execute(&sb, data); err != nil {
		panic(pkg_errors.WithMessagef(err, "export code: csharp: %s", name))
	}
	return strings.TrimRight(sb.String(), "\n")
}

